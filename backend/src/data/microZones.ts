export function getMicroZoneById(id: string): MicroZone | undefined {
  return microZones.find(z => z.id === id);
}

export function getMicroZonesInArea(lat: number, lng: number, radiusMiles: number): MicroZone[] {
  const milesPerDegree = 69;
  const radiusDegrees = radiusMiles / milesPerDegree;
  
  return microZones.filter(zone => {
    const latDiff = Math.abs(zone.coordinates.lat - lat);
    const lngDiff = Math.abs(zone.coordinates.lng - lng);
    const distance = Math.sqrt(latDiff ** 2 + lngDiff ** 2);
    return distance <= radiusDegrees;
  });
}

